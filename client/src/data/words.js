export const getRandomWords = (count = 25) => {
  const shuffled = [...WORD_LIST].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const generateCardTypes = () => {
  const types = [];
  const firstTeam = Math.random() > 0.5 ? CARD_TYPES.RED : CARD_TYPES.BLUE;
  const secondTeam = firstTeam === CARD_TYPES.RED ? CARD_TYPES.BLUE : CARD_TYPES.RED;
  
  for (let i = 0; i < 9; i++) types.push(firstTeam);
  for (let i = 0; i < 8; i++) types.push(secondTeam);
  for (let i = 0; i < 7; i++) types.push(CARD_TYPES.NEUTRAL);
  types.push(CARD_TYPES.ASSASSIN);
  
  return types.sort(() => Math.random() - 0.5);
};

export const createGameBoard = () => {
  const words = getRandomWords(25);
  const cardTypes = generateCardTypes();
  
  return words.map((word, index) => ({
    id: index,
    word,
    type: cardTypes[index],
    revealed: false
  }));
};
